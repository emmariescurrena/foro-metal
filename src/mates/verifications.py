"""Mathematical verifications functions"""


def is_odd(number):
    """Returns 'True' if number is odd, else 'False'"""

    if number % 2 == 1:
        return True

    return False


def is_even(number):
    """Returns 'True' if number is even, else 'False'"""

    if number % 2 == 0:
        return True

    return False
